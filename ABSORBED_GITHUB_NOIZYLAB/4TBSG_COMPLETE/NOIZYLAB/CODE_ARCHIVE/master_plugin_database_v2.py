def get_manufacturer_info(library_name: str):
    """Get manufacturer information from library name with priority scoring"""
    library_lower = library_name.lower()
    
    best_match = None
    best_score = 0
    
    for manufacturer, info in MASTER_DATABASE.items():
        for keyword in info['keywords']:
            if keyword.lower() in library_lower:
                # Score is priority + keyword length (longer = more specific)
                score = info.get('priority', 5) + len(keyword)
                if score > best_score:
                    best_score = score
                    best_match = {
                        'manufacturer': manufacturer,
                        'type': info['type'],
                        'original_name': library_name
                    }
    
    if best_match:
        return best_match
    
    return {
        'manufacturer': 'Unknown',
        'type': 'unknown',
        'original_name': library_name
    }

