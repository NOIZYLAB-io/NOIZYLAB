"""
This is a simple Python function named 'main'. 

The function initializes two variables 'a' and 'b' with values 10 and 12 respectively. 
Then, it adds these two variables and stores the result in another variable 'c'. 
Finally, it returns the value of 'c'. 
"""
def main():
    """
    Initializes two variables 'a' and 'b' with values 10 and 12 respectively.
    Adds these two variables and stores the result in another variable 'c'.
    Returns the value of 'c'.
    
    :return: The sum of 'a' and 'b'.
    :rtype: int
    """
    a = 10  # Initialize variable 'a' with the value 10
    b = 12  # Initialize variable 'b' with the value 12
    c = a + b  # Add the values of 'a' and 'b' and store the result in variable 'c'

    return c  # Return the value of 'c'